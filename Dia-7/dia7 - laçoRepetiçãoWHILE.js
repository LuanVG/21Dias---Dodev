// Trabalhando com laço de repetição WHILE

// Laço de repetição WHILE : enquanto a condição for verdadeira, o laço de repetição será executado.

//WHILE
let idade = 18
while (idade >= 18) {
    idade = Number(prompt('Qual a sua idade?'))
    console.log(idade)
    }
console.log('Você é menor de idade');

// Explicação do código acima:
// A variável idade é inicializada com o valor 18.
// O laço de repetição WHILE é iniciado.
// A condição do laço de repetição é verificada.
// Se a idade for maior ou igual a 18, o laço de repetição é executado.
// O usuário é questionado sobre a idade.
// A idade é exibida no console.
// A condição do laço de repetição é verificada novamente.
// Se a idade for maior ou igual a 18, o laço de repetição é executado novamente.
// se caso a idade for menor que 18, o laço de repetição é encerrado.

