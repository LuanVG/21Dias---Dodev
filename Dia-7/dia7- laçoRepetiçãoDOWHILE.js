// Laço de repetição DO WHILE : o laço de repetição será executado pelo menos uma vez, mesmo que a condição seja falsa.

// Trabalhando com laço de repetição DO WHILE

//DO WHILE

let idade = 17

do {
    idade = Number(prompt('Qual a sua idade?')) 
    console.log(idade)
    
} while (idade >= 18); 

console.log('Você é menor de idade');

// Explicação do código acima: 
// A variável idade é inicializada com o valor 17.
// O laço de repetição DO WHILE é iniciado.
// O usuário é questionado sobre a idade.
// A idade é exibida no console.
// A condição do laço de repetição é verificada.
// Se a idade for maior ou igual a 18, o laço de repetição é encerrado.
// Caso contrário, o laço de repetição é executado novamente.
// O laço de repetição é executado pelo menos uma vez, mesmo que a condição seja falsa.
